import api from "@/lib/api";
import { de } from "zod/v4/locales";

export const ownersService = {
  /*
  |--------------------------------------------------------------------------
  | Get owners list
  |--------------------------------------------------------------------------
  */
  getAll: (params = {}) => api.get("/owner/owners", { params }),

  /*
  |--------------------------------------------------------------------------
  | Get one owner
  |--------------------------------------------------------------------------
  */
  getOne: (id) => api.get(`/owner/owners/${id}`),

  /*
  |--------------------------------------------------------------------------
  | Update owner
  |--------------------------------------------------------------------------
  */
  update: (id, data) => api.post(`/owner/owners/${id}`, data),

  /*
  |--------------------------------------------------------------------------
  | Delete owner
  |--------------------------------------------------------------------------
  */
  remove: (id) => api.delete(`/owner/owners/${id}`),

  deleteImage: (ownerId, path) =>
    api.delete(`/owner/owners/${ownerId}/image`, {
      data: { path },
    }),
};
